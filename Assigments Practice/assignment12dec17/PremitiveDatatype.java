package assignment12dec17;

public class PremitiveDatatype {
	static int i;
	static long l;
	static String s;
	static boolean b;
	static float f;
	static double d;

	public static void main(String[] args) {

		{
			System.out.println("Default value for all primitive values");
			System.out.println("int : " + i);
			System.out.println("long : " + l);
			System.out.println("String : " + s);
			System.out.println("Boolean : " + b);
			System.out.println("Float : " + f);
			System.out.println("double : " + d);
		}

	}
}

//output
//Default value for all primitive values
//int : 0
//long : 0
//String : null
//Boolean : false
//Float : 0.0
//double : 0.0
