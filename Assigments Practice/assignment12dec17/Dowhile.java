package assignment12dec17;

public class Dowhile {
	public static void main(String[] args) {
		int a = 3;

		do {
			a++;
			{
				System.out.println(a);

			}

		} while (a <= 7);
		System.out.println("Out of Do while loop  ");
	}
}

